/* kernelvec.S — 内核陷阱入口汇编（Lab4 任务2）
 *
 * 当 CPU 处于 S-Mode 并发生中断或异常时，硬件会：
 *   1. 将 PC 保存到 sepc
 *   2. 跳转到 stvec 寄存器指定的地址（即这里的 sys_trap_vector 标签）
 *
 * 本文件的任务：
 *   1. 保存当前所有寄存器到栈上（不能丢失任何状态！）
 *   2. 调用 C 函数 sys_trap_handler() 处理中断原因
 *   3. 从栈上恢复所有寄存器
 *   4. 用 sret 返回到被打断的代码继续执行
 *
 * 注意：保存和恢复的顺序必须完全镜像对称！否则寄存器会"张冠李戴"。
 */

    .globl sys_trap_vector
    .align 4

sys_trap_vector:
    # =============================================================
    # 步骤1：在栈上分配 32*8 = 256 字节的空间
    # 用于保存全部 32 个通用寄存器（每个 64 位 = 8 字节）
    # 注意：x0（zero）永远是 0，但为了统一偏移量，也留一个槽位
    # =============================================================
    addi sp, sp, -256

    # 保存各寄存器到栈（每个 sd 指令存 8 字节）
    # 格式：sd <寄存器>, <从sp的偏移量>(sp)
    sd ra,  0(sp)
    sd gp,  16(sp)
    sd tp,  24(sp)
    sd t0,  32(sp)
    sd t1,  40(sp)
    sd t2,  48(sp)

    # =============================================================
    # TODO [Lab4-任务2-步骤1]：
    #   继续保存剩余寄存器。请严格按照偏移量递增（每次加8）填写：
    #   s0(fp) 在 56(sp)，s1 在 64(sp)，a0 在 72(sp)，a1 在 80(sp)
    #   a2-a7 功能依次在 88~128(sp)，s2-s11 在 136~208(sp)
    #   t3 在 216(sp)，t4 在 224(sp)，t5 在 232(sp)，t6 在 240(sp)
    #   注意：sp 自身（8(sp)）跳过，稍后单独处理
    # =============================================================
    sd s0,  56(sp)
    sd s1,  64(sp)
    sd a0,  72(sp)
    sd a1,  80(sp)
    sd a2,  88(sp)
    sd a3,  96(sp)
    sd a4,  104(sp)
    sd a5,  112(sp)
    sd a6,  120(sp)
    sd a7,  128(sp)
    sd s2,  136(sp)
    sd s3,  144(sp)
    sd s4,  152(sp)
    sd s5,  160(sp)
    sd s6,  168(sp)
    sd s7,  176(sp)
    sd s8,  184(sp)
    sd s9,  192(sp)
    sd s10, 200(sp)
    sd s11, 208(sp)
    sd t3,  216(sp)
    sd t4,  224(sp)
    sd t5,  232(sp)
    sd t6,  240(sp)

    # =============================================================
    # 步骤2：保存 sp。注意：此时 sp 已经被减去了 256，
    #   所以要保存的是"减去 256 之前"的原始 sp 值
    # =============================================================
    addi t0, sp, 256   # t0 = 进入 sys_trap_vector 前的原始 sp 值
    sd   t0, 8(sp)     # 保存原始 sp 到槽位 8(sp)

    # =============================================================
    # 步骤3：所有寄存器现已安全存入栈中，可以调用 C 函数
    # =============================================================
    call sys_trap_handler

    # =============================================================
    # 步骤4：从栈上恢复所有寄存器（顺序必须与保存时完全镜像）
    # 注意：sp 要最后恢复，否则后续 ld 指令的基地址会出错
    # =============================================================
    ld ra,  0(sp)
    # （跳过 sp，最后恢复）
    ld gp,  16(sp)
    ld tp,  24(sp)
    ld t0,  32(sp)
    ld t1,  40(sp)
    ld t2,  48(sp)

    # =============================================================
    # TODO [Lab4-任务2-步骤2]：
    #   用 ld 指令恢复上面保存的所有寄存器，顺序必须与 sd 对称。
    #   最后恢复 sp：必须是最后一步！
    #   如果使用 ld sp, 8(sp) 来恢复 sp，则删除下面这行 addi；
    #   如果不用 ld 恢复 sp，则保留 addi 来恢到栈顶。
    # =============================================================

    ld s0,  56(sp)
    ld s1,  64(sp)
    ld a0,  72(sp)
    ld a1,  80(sp)
    ld a2,  88(sp)
    ld a3,  96(sp)
    ld a4,  104(sp)
    ld a5,  112(sp)
    ld a6,  120(sp)
    ld a7,  128(sp)
    ld s2,  136(sp)
    ld s3,  144(sp)
    ld s4,  152(sp)
    ld s5,  160(sp)
    ld s6,  168(sp)
    ld s7,  176(sp)
    ld s8,  184(sp)
    ld s9,  192(sp)
    ld s10, 200(sp)
    ld s11, 208(sp)
    ld t3,  216(sp)
    ld t4,  224(sp)
    ld t5,  232(sp)
    ld t6,  240(sp)

    addi sp, sp, 256   # 如果不用最后的 ld sp 恢复，用这行代替

    # =============================================================
    # 步骤5：sret — 从内核陷阱返回
    #   硬件会自动将 PC 设置为 sepc 寄存器中保存的地址，
    #   恢复中断使能状态（sstatus.SIE），返回被打断的代码继续执行。
    # =============================================================
    sret
