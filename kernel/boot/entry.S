/* entry.S — 内核引导汇编（Lab1 任务2）
 *
 * 这是内核被 QEMU 加载后执行的第一段代码。
 * CPU 在 0x80000000 处开始无脑执行这里的指令。
 *
 * 你的目标：
 *   1. 在内存中为 C 语言划定一块"栈"区域
 *   2. 设置栈指针寄存器 sp 指向栈顶
 *   3. 跳转到 C 函数 start_main()
 */

    .section .text.entry     /* 告诉链接器：把我放在 .text.entry 节，保证排最前面 */
    .global _entry           /* 声明 _entry 为全局符号，供 kernel.ld 的 ENTRY() 使用 */

_entry:
    # =============================================================
    # TODO [Lab1-任务2-步骤1]：
    #   用 la 指令将 stack0 的地址加载到 sp 寄存器中。
    #   语法：la  <目标寄存器>,  <标签名>
    # =============================================================
    la sp, stack0

    # =============================================================
    # TODO [Lab1-任务2-步骤2]：
    #   用 li 指令将 4096（即一页大小）加载到 a0 寄存器。
    #   这是我们分配给栈的字节数。
    #   语法：li  <目标寄存器>,  <立即数>
    # =============================================================
    li a0, 4096

    # =============================================================
    # TODO [Lab1-任务2-步骤3]：
    #   思考：RISC-V 的栈是从高地址向低地址生长的。
    #   所以 sp 必须指向栈区域的"顶部"（地址最高处）。
    #   用 add 指令，将 sp 加上 a0，使 sp 指向栈顶。
    #   语法：add  <目标寄存器>,  <rs1>,  <rs2>
    # =============================================================
    add sp, sp, a0

    # =============================================================
    # TODO [Lab1-任务2-步骤4]：
    #   栈准备好了！现在跳转到 C 语言主函数。
    #   本框架中入口函数名叫 start_main（不是标准的 main）。
    #   语法：call  <函数名>
    # =============================================================
    call start_main

spin:
    # 如果 C 函数意外返回（不应该），进入死循环防止 PC 乱跑
    j spin

    # ------------------------------------------------------------------
    # 下方是预先在内存中划出的 4096 字节栈空间
    # .bss 节：程序加载时自动清零，不占 ELF 文件体积
    # .align 4：按 2^4=16 字节对齐（RISC-V 栈对齐要求）
    # ------------------------------------------------------------------
    .section .bss
    .align 4
stack0:
    .space 4096
