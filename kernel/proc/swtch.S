/* swtch.S — 内核上下文切换汇编（Lab5 任务2）
 *
 * 函数签名：void swtch(struct context *old, struct context *new);
 *
 * 调用约定（RISC-V ABI）：
 *   a0 = old（指向要保存当前寄存器的 context 结构）
 *   a1 = new（指向要恢复的目标 context 结构）
 *
 * 这个函数做了一件神奇的事：
 *   - 调用前：CPU 在"旧"进程（或调度器）的代码里运行
 *   - 函数返回后：CPU 神不知鬼不觉地在"新"进程（或调度器）的代码里运行了！
 *   - 秘密：ret 指令会把 PC 设置为 ra 寄存器的值，
 *           而我们在恢复 ra = new->ra，所以 ret 后会跳到新进程设定的位置。
 *
 * 注意：swtch 只保存 Callee-Saved 寄存器！
 *   因为 swtch() 是被 C 代码调用的普通函数，
 *   Caller-Saved 寄存器（t0~t6, a0~a7）已在函数调用前被 C 编译器自动处理。
 */

    .globl swtch
swtch:
    # =========================================================
    # 步骤1：将当前 CPU 的内核寄存器保存到 old（a0）指向的 context 中
    # struct context 的内存布局（从proc.h读取确认）：
    #   偏移 0  : s0
    #   偏移 8  : s1
    #   偏移 16 : s2
    #   ... 以此类推到 s11（偏移 88）
    #   偏移 96 : ra
    #   偏移 104: sp
    # =========================================================
    sd s0,   0(a0)
    sd s1,   8(a0)
    sd s2,  16(a0)
    sd s3,  24(a0)

    # =============================================================
    # TODO [Lab5-任务2-步骤1]：
    #   继续保存 s4~s11, ra, sp 到 a0 指向的地址中。
    #   s4~s11 偏移量依次为 32, 40, ... 到 88。
    #   ra 偏移量为 96，sp 偏移量为 104。
    # =============================================================
    # sd s4,  32(a0)
    # sd s5,  40(a0)
    # ... 补充完整

    # =========================================================
    # 步骤2：从 new（a1）指向的 context 中恢复寄存器到 CPU
    # 顺序必须与上面完全镜像对称！
    # =========================================================
    ld s0,   0(a1)
    ld s1,   8(a1)
    ld s2,  16(a1)
    ld s3,  24(a1)

    # =============================================================
    # TODO [Lab5-任务2-步骤2]：
    #   继续从 a1 指向的地址恢复 s4~s11, ra, sp。
    #   偏移量与上面的保存操作完全对应。
    # =============================================================
    # ld s4,  32(a1)
    # ld s5,  40(a1)
    # ... 补充完整

    # =========================================================
    # 魔法时刻！
    # ret 指令等价于 jalr zero, ra, 0
    # 即：将 PC 设置为刚才从 new->ra 恢复的 ra 值
    # 从此，CPU 就"跳进"了新进程（或调度器）的代码！
    # =========================================================
    ret
